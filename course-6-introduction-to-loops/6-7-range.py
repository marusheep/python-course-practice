# range()

# 1. use range() with 1 argument
one_input = range(5)
for num in one_input:
    print(num)


# 2. use range() with 2 arguments
two_input = range(3,10)
for num in two_input:
    print(num)

# 3. use range() with 3 arguments
three_input = range(5,30,2)
for num in three_input:
    print(num)